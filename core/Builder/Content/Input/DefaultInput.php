class DefaultInput extends DefaultContent implements FormElement
{

	/**
	 * @var string
	 */
	protected $type = FormTypes::TEXT;

	/**
	 * @var string
	 */
	protected $value = "";

	/**
	 * DefaultInput constructor.
	 *
	 * @param string $value
	 * @param string $type
	 */
	public function __construct(string $value, string $type)
	{
		if (!empty($type)) $this->type = $type;
		parent::__construct("input");
		$this->value = $value;
	}

	/**
	 * @inheritDoc
	 */
	public function getHtml(): string
	{
		//update value on property list, because they donÂ´t able to overwrite
		$this->addPropertyValue("value", $this->getValue());
		$this->addPropertyValue("type", $this->type);
		return parent::getHtml(); // TODO: Change the autogenerated stub
	}

	/**
	 * @return string
	 */
	public function getValue(): string
	{
		return $this->value;
	}

	/**
	 * @inheritdoc
	 */
	public function getContent(): string
	{
		return "";
	}
}